void MainWindow::on_ButtonConnect_clicked()
{
    if (ui->actionConnect->isEnabled()) {
        ui->ButtonConnect->setText("Disconnect");
        apply();
        openSerialPort();
    }
    else {
        ui->ButtonConnect->setText("Connect");
        closeSerialPort();
    }
}

void MainWindow::on_applyButton_clicked()
{
    apply();
}

void MainWindow::on_clearScreen_clicked()
{
    console->clear();
}

void MainWindow::on_startLog_clicked()
{
    if (log_enable == false) {
        name_logfile = file_log->getSaveFileName(file_log, "Save file", "", ".log");
        logfile = new QFile( name_logfile + ".log" );

        if (!logfile->open( QIODevice::WriteOnly ))
            log_enable = false;
        else {
            ui->startLog->setText("Stop Log");
            log_enable = true;
        }
    }
    else {
        ui->startLog->setText("Start Log");
        log_enable = false;
        logfile->close();
    }
}

void MainWindow::on_send_file_clicked()
{
   QFile file(file_send->getOpenFileName(file_send, tr("Open File"), "", tr("All")));

   if (!file.open(QIODevice::ReadOnly))
       return;
   else {
       QByteArray data;
       while(!file.atEnd()) {
            data = file.readLine() + "\n";
            writeData(data);
       }
   }
}
